class Solution(object):
    def searchInsert(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        left=0
        right=len(nums)-1
        while(left<=right):
            m=(left+right)//2
            if nums[m]==target:
                return m
            if target>nums[m]:
                left=m+1
            else:
                right=m-1
        return left
        

